import XCTest
@testable import PitchSpellingTools

class NamedIntervalTests: XCTestCase {

    func testInitPerfectUnisonNotNil() {
        XCTAssertNotNil(NamedInterval(.perfect, .unison))
    }

    func testInitDiminishedSecondNotNil() {
        XCTAssertNotNil(NamedInterval(.diminished, .second))
    }
    
    func testInitMajorFifthNil() {
        XCTAssertNil(NamedInterval(.major, .fifth))
    }
    
    func testQuadrupleAugmentedSeventhNotNil() {
        XCTAssertNotNil(NamedInterval(.quadruple, .augmented, .seventh))
    }
    
    func testInitDoubleDiminishedFourthNotNil() {
        XCTAssertNotNil(NamedInterval(.double, .diminished, .fourth))
    }
    
    func testMajorSecondInverseMinorSeventh() {
        let majorSecond = NamedInterval(.major, .second)!
        let minorSeventh = NamedInterval(.minor, .seventh)!
        XCTAssertEqual(majorSecond.inverse, minorSeventh)
    }
    
    func testDoubleAugmentedFifthInverseDoubleDiminishedFourth() {
        let doubleAugmentedFifth = NamedInterval(.double, .augmented, .fifth)!
        let doubleDiminishedFourth = NamedInterval(.double, .diminished, .fourth)!
        XCTAssertEqual(doubleAugmentedFifth.inverse, doubleDiminishedFourth)
    }
    
    func testQualityDegreeNil() {
        XCTAssertNil(NamedInterval.Quality.major[.double])
        XCTAssertNil(NamedInterval.Quality.minor[.triple])
    }
    
    func testQualitySingleNotNil() {
        XCTAssertNotNil(NamedInterval.Quality.major[.single])
    }
    
    func testInitUnisonQuarterToneAugmentedNotNil() {
        XCTAssertNotNil(NamedInterval(steps: 0, intervalClass: 0.5))
    }
}
